package sample;

public class StringReverce {
	
	public void add(int a,int b)
	{
	System.out.println(a+b +"it is sum of a and b");	
	}
	public void add(float a,float b)
	{
	System.out.println(a+b +"it is sum of a and b");	
	}
	
	
public static void main(String[] args) {
	StringReverce s=new StringReverce();
	s.add(1,6);
}
}
